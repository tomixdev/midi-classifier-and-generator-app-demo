def do_all_class_attributes_of_classA_inherit_some_class_attributes_of_classB(classA, classB):
    assert inspect.isclass(classA)
    assert inspect.isclass(classB)

    classA_attributes = get_dict_of_user_defined_public_class_attributes_excluding_methods(
        classA)
    classB_attributes = get_dict_of_user_defined_public_class_attributes_excluding_methods(
        classB)
    return set(classA_attributes.keys()).issubset(classB_attributes.keys())


def does_a_func_belong_to_a_class(a_func_obj, a_cls):
    assert inspect.isfunction(a_func_obj)
    assert inspect.isclass(a_cls)
    return a_func_obj.__qualname__.startswith(a_cls.__name__ + '.')


def get_dict_of_user_defined_public_class_attributes_excluding_methods(a_class: object):
    """
    Loosely Tested on 20221115
    This function returns DICT of 'variable_name: value'

    This function does NOT return instance methods, class methods, static methods!!!
    """
    assert inspect.isclass(a_class)

    dict_to_return = {}
    for k, v in a_class.__dict__.items():
        dict_to_return[k] = v

    list_of_keys_to_remove_from_dict_to_return = []
    for k, v in dict_to_return.items():
        condition_to_remove_a_key = (
            isinstance(v, classmethod) or
            isinstance(v, staticmethod) or
            k.startswith('__') or
            k.startswith('-') or
            inspect.ismethod(v) or
            inspect.isclass(v) or
            (inspect.isfunction(v) and does_a_func_belong_to_a_class(
                a_func_obj=v, a_cls=a_class))
        )
        if condition_to_remove_a_key:
            list_of_keys_to_remove_from_dict_to_return.append(k)

    for a_key in list_of_keys_to_remove_from_dict_to_return:
        dict_to_return.pop(a_key)

    return dict_to_return



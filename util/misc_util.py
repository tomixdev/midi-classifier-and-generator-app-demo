def ensure_a_value_is_a_number(a_value):
    if a_value is None:
        raise Exception(f'{a_value} is not a number')
    if np.char.isnumeric(str(a_value)):
        return Exception(f"{a_value} is not a number")
    if isinstance(a_value, np.number):
        return True
    if hasattr(a_value, 'dtype') and np.issubdtype(a_value.dtype, np.number):
        return True
    if isinstance(a_value, int) or isinstance(a_value, float):
        return True

    raise Exception(f'{a_value} is not a number')



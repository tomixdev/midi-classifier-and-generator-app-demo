def convert_time_point_tuple_in_s_to_tuple_in_minutes_and_seconds(a_time_point_tuple, cut_off_floating_points_in_seconds=True):
    assert isinstance(a_time_point_tuple, tuple), type(a_time_point_tuple)
    assert isinstance(cut_off_floating_points_in_seconds, bool), type(
        cut_off_floating_points_in_seconds)
    assert len(a_time_point_tuple) == 2
    assert isinstance(a_time_point_tuple[0], numbers.Number)
    assert isinstance(a_time_point_tuple[1], numbers.Number)

    return (
        convert_integer_to_minutes_and_seconds_string(
            a_num_value=a_time_point_tuple[0], cut_off_floating_points_in_seconds=cut_off_floating_points_in_seconds),
        convert_integer_to_minutes_and_seconds_string(
            a_num_value=a_time_point_tuple[1], cut_off_floating_points_in_seconds=cut_off_floating_points_in_seconds)
    )


def convert_integer_to_minutes_and_seconds_string(a_num_value, cut_off_floating_points_in_seconds=True):
    assert isinstance(a_num_value, (int, float))
    assert a_num_value >= 0

    minutes = math.floor(a_num_value / 60)
    seconds = a_num_value - minutes * 60

    if cut_off_floating_points_in_seconds:
        seconds = math.floor(seconds)

    return str(minutes) + 'm' + str(seconds) + 's'


def convert_float_num_tuple_str_to_tuple(a_tuple_str):
    assert isinstance(a_tuple_str, str)
    assert a_tuple_str[0] == '('
    assert a_tuple_str[-1] == ')'
    return tuple(map(float, a_tuple_str[1:-1].split(',')))



def varnameof(var, get_string_after_dot=True):
    '''
    TODO: Not Confirmed that this function always works. Copy and Pasted from:
    https://stackoverflow.com/questions/18425225/getting-the-name-of-a-variable-as-a-string
    '''

    stacks = inspect.stack()
    try:
        func = stacks[0].function
        code = stacks[1].code_context[0]
        s = code.index(func)
        s = code.index("(", s + len(func)) + 1
        e = code.index(")", s)

        varname_string_to_return = code[s:e].strip()

        if get_string_after_dot == True:
            varname_string_to_return = varname_string_to_return.split('.')[-1]

        return varname_string_to_return
    except Exception as e:
        raise Exception(e)
        return ""


